"""
Standalone generator for arm_transpose_conv_s16 test data.

Uses TensorFlow Lite with the int16x8 quantization scheme:
  - Input:   int16, zero_point = 0  (symmetric)
  - Weights: int8
  - Bias:    int64
  - Output:  int16, zero_point = 0  (symmetric)

Run from the Tests/UnitTest directory:
  python generate_transpose_conv_s16_data.py

Outputs C header files under TestCases/TestData/<test_name>/
"""

import math
import os
import sys
import numpy as np
import tensorflow as tf
import keras

